/*
Create a function named `addNumbers` that accepts two arguments and returns their sum.
Export `addNumbers` function as a named export.
Create a function named `multiplyNumbers` that accepts two arguments and returns their product.
Export `multiplyNumbers` function as a default export
*/

export function addNumbers(num1, num2) {
  return num1 + num2;
}

export default function multiplyNumbers(num1, num2) {
  return num1 * num2;
}
